void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	gecici;

	while (size >= 0)
	{
		i = 0;
		while (i < size - 1)
		{
			if (tab[i] > tab[i + 1])
			{
				gecici = tab[i];
				tab[i] = tab[i + i];
				tab [i + 1] = gecici;
			}
			i++;
		}
		size--;
	}
}